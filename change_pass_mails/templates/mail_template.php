Pourquoi mon mot de passe change-t-il ?

    
    Par sécurité. Ce serveur héberge de très nombreuses boîtes mails qui dépendent les unes des autres en termes de sécurité. 
    
    Exemple: une boite mail avec un mot de passe faible est piratée. Cela va donner une mauvaise réputation au serveur dans son ensemble : les mails des autres utilisateurs vont être bloqués.
    
    Or c'est exactement ce problème que nous rencontrons : nous avons constaté une faiblesse générale des mots de passe sur alice qui peut vous nuire.
    
    Les mots de passe de nos utilisateurs sont chiffrés, nous devons donc les réinitialiser tous en forçant généralement des critères de sécurité plus élevés.
    

Nous vous enverrons un nouveau message vous rappelant votre nouveau mot de passe la veille de la mise à jour.
    
Nous sommes sincèrement désolés de la gêne que cette opération pourrait occasionner.
    
